const quizData = adicionarImagens(quizDataJogador);

function normalizeString(str) {
  if (!str || typeof str !== "string") {
    return "";
  }

  return str
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, "")
    .trim();
}

function evaluateAnswer(jogadorId, userInput) {
  const jogador = quizData.find((j) => j.id === jogadorId);
  if (!jogador) {
    return { valido: false, mensagem: "Jogador não encontrado." };
  }

  const inputNormalizado = normalizeString(userInput);
  if (!inputNormalizado) {
    return { valido: false, mensagem: "Resposta vazia." };
  }

  const palavrasInput = inputNormalizado.split(/\s+/);

  const respostaValida = jogador.respostas.some((resposta) => {
    const respostaNormalizada = normalizeString(resposta);
    if (!respostaNormalizada) return false;
    const palavrasResposta = respostaNormalizada.split(/\s+/);
    return palavrasResposta.every((palavra) => palavrasInput.includes(palavra));
  });

  if (respostaValida) {
    return { valido: true, mensagem: "Resposta correta!" };
  }

  return { valido: false, mensagem: "Resposta incorreta. Tente novamente." };
}

